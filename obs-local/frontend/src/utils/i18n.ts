export type UiTextKey = keyof typeof UI_TEXT;

export function renderLocale(mode: UiLocaleMode, zh: string, en: string): string {
  if (mode === "zh") {
    return zh;
  }
  if (mode === "en") {
    return en;
  }
  return `${zh} / ${en}`;
}

export function translateUiText(mode: UiLocaleMode, key: UiTextKey): string {
  const entry = UI_TEXT[key];
  return renderLocale(mode, entry.zh, entry.en);
}
